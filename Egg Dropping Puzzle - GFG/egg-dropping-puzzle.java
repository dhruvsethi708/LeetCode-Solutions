class Solution 
{
    //Function to find minimum number of attempts needed in 
    //order to find the critical floor.
    static int eggDrop(int n, int k) 
	{
	    // Your code here
	    
	    int[][] dp = new int[n+1][k+1];
        int m = 0;
        
        while(dp[n][m] < k){
            m+=1;
            
            for(int i=1; i<=n; i++){
                dp[i][m] = dp[i][m-1] + dp[i-1][m-1] + 1;
            }
        }
        
        return m;
	}
}